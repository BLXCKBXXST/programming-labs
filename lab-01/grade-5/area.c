#include <stdio.h>
#include <math.h>

int main(void) {
    double x, y;
    printf("x y: ");
    if (scanf("%lf %lf", &x, &y) != 2) {
        printf("Invalid input\n");
        return 1;
    }

    /*
     * Вариант 8 (Скорпион). Область из 1.pdf:
     *   - верхняя половина квадрата [-1, 1] × [0, 1]  — закрашена полностью;
     *   - в нижней половине закрашены два боковых треугольника
     *     над прямыми y = x   (для x ≥ 0)   и   y = -x (для x ≤ 0),
     *     центральный «нос» y < -|x| — не закрашен.
     *
     * Совместная запись: y ≥ -|x|, при условии что точка внутри
     * объемлющего квадрата [-1, 1] × [-1, 1].
     */
    int inside =
        (x >= -1.0 && x <= 1.0) &&
        (y >= -1.0 && y <= 1.0) &&
        (y >= -fabs(x));

    printf("%s\n", inside ? "YES" : "NO");
    return 0;
}
